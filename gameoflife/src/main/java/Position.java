package main.java;

public class Position
{
    private final int x;

    private int y;

    public Position(int x, int y)
    {
        //To change body of created methods use File | Settings | File Templates.
        this.x = x;
        this.y = y;
    }

    public int getY()
    {
        return y;
    }

    public int getX()
    {
        return x;
    }
}
